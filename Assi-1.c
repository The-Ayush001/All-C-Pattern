// WAp to calculate & display the volume of a CUBE having X(h=10 cm),(w=10 cm)& depth(10 cm)
#include<stdio.h>
#include<math.h>
int main(){
    int height,width,depth,vol;
    height=10;
    width=10;
    depth=10;
    vol=height*width*depth;
    printf("Volume of a CUBE =%d",vol);
    return 0;
}